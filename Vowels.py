# String Value
string = "Guvi Geeks Network Private Limited"
# Convert to lower case
string = string.lower()
#Counting each vowels
vowel_a = string.count('a')
vowel_e = string.count('e')
vowel_i = string.count('i')
vowel_o = string.count('o')
vowel_u = string.count('u')
# Calculating total vowels
total_vowels = vowel_a + vowel_e + vowel_i + vowel_o + vowel_u

#Output
print("TOTAL NUMBER OF VOWELS:", total_vowels)
print("A =", vowel_a)
print("E =", vowel_e)
print("I =", vowel_i)
print("O =", vowel_o)
print("U =", vowel_u)